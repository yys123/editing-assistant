def _load_file(name: str) -> Optional[str]:
    path = os.path.join(_DATA_DIR, name)
    if os.path.exists(path):
        with open(path, encoding="utf-8") as f:
            text = f.read()
            return text if text.strip() else None
    return None


def _normalize_entry_type(entry_type: Optional[str]) -> str:
    return entry_type if entry_type in {"disease", "tumor", "non_disease"} else "disease"


def _load_standard_file(entry_type: Optional[str], filename: str) -> Optional[str]:
    normalized_entry_type = _normalize_entry_type(entry_type)
    prefixed_filename = f"{normalized_entry_type}_{filename}"
    path = os.path.join(_STANDARDS_DIR, normalized_entry_type, prefixed_filename)
    if os.path.exists(path):
        with open(path, encoding="utf-8") as f:
            text = f.read()
            return text if text.strip() else None
    return None


